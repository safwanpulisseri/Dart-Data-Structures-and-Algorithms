/// Trimming Whitespace in Dart
///
/// Dart provides the `trim` method to remove leading and trailing whitespace from a string.
/// The `trim` method returns a new string with the whitespace removed.
///
/// Example:
void main() {
  String spacedStr = "   Hello, Dart!   ";
  String trimmedStr = spacedStr.trim(); 
  print(trimmedStr);
}
