/// Concatenation in Dart
///
/// Definition:
/// Concatenation is a process of combining or linking two or more strings to form a single string.
/// It is a fundamental operation when working with strings in programming.
///
/// Purpose:
/// The primary purpose of concatenation is to create a new string by joining the contents of existing strings.
/// This allows for the creation of more complex strings, often used in generating output, building messages,
/// or manipulating text.
void main() {
  String greeting = "Hello";
  String name = "Raju";

  // Concatenation Example:
  String message = greeting + " " + name;

  print(message); // Output: "Hello Raju"
}
